using namespace std;

void solve(){
    int n,ans=0;
    cin >> n;
    int a[n],b[n];
    for(int i=0;i<n;i++){
        cin >> a[i];
    }
    for(int i=0;i<n;i++){
        cin >> b[i];
    }
    for(int i=0;i<n;i++){
        int diff = a[i]-b[i];
        ans+=abs(diff);
    }
    cout << ans << "\n";
}

