void solve(){
    int n,ans = 0, sum = 0;
    cin >> n;
    vi a(n);
    for(int i=0;i<n;i++){
        cin >> a[i];
    }
    sort(a.begin(),a.end());
    for(int i=0;i<n;i++){
        sum+=a[i];
        double avg = (double)(sum)/(i+1);
        int pos = upper_bound(a.begin(),a.end(),avg) - a.begin();
        ans = max(ans,i-pos+1);
    }
    cout << ans << "\n";
}

